// arithmetic số học
console.log("+:", 1 + 1);
console.log("-:", 1 - 1);
console.log("*:", 1 * 1);
console.log("/:", Math.floor(4 / 3)); // float

console.log("**:", 3 ** 2); // power
console.log("%:", 4 % 3);

// logical
console.log("&&: ", true && true); // true
console.log("&&: ", false && true); // false
console.log("||: ", false || true); // true
console.log("||: ", false || false); // false

// relational
console.log(">: ", 1 > 0);
console.log(">=: ", 1 >= 0);
console.log("<: ", 1 < 0);
console.log("<=: ", 1 <= 0);

console.log("==: ", 0 == "");
console.log("===: ", 0 === "");
console.log("!=: ", 0 != "");
console.log("!==: ", 0 !== "");
console.log("string", "auanh" < "abc");

